"""Main per-rank tokenization loop for the distributed vision pipeline.

Closely follows audio_tokenization/pipelines/lhotse/core.py but replaces
the DataLoader/sampler pattern with direct iteration over a pre-computed
``BatchPlan``.  Checkpoint = batch_index (deterministic).

Launch examples::

    # Single node, 4 GPUs
    srun --ntasks-per-node=4 --gpus-per-node=4 \
        python -m vision_tokenization.tokenize mode=image2text dataset=pmc_oa num_gpus=4

    # Resume from checkpoint
    srun --ntasks-per-node=4 --gpus-per-node=4 \
        python -m vision_tokenization.tokenize mode=image2text dataset=pmc_oa num_gpus=4 resume=true
"""

import logging
import os
from pathlib import Path
from typing import Any, Dict

import torch

from vision_tokenization.indexing.clustered_batch_planner import (
    BatchPlan,
    plan_clustered_batches,
)
from .checkpoint import (
    WorkerStats,
    SimpleWandbLogger,
    is_cuda_oom,
    load_checkpoint,
    save_checkpoint,
)
from .data import ImageAugmenter, create_loader
from .prefetch import BatchPrefetcher

logger = logging.getLogger(__name__)


def _resolve_multi_image(cfg: Dict[str, Any]) -> bool:
    """Resolve whether the dataset should use logical multi-image handling."""
    explicit = cfg.get("multi_image")
    if explicit is not None:
        return bool(explicit)
    return cfg.get("image_list_column") is not None


def _load_or_compute_batch_plan(cfg: Dict[str, Any], is_multi_image: bool) -> BatchPlan:
    """Load a pre-computed BatchPlan from file, or compute one from the manifest."""
    plan_path = cfg.get("batch_plan_path")
    if plan_path and Path(plan_path).exists():
        logger.info(f"Loading pre-computed batch plan from {plan_path}")
        # weights_only=False required: BatchPlan is a dataclass with numpy arrays.
        # Safe here because plan files are generated by our own pipeline.
        plan = torch.load(plan_path, map_location="cpu", weights_only=False)
        if isinstance(plan, BatchPlan):
            return plan
        raise TypeError(f"Expected BatchPlan, got {type(plan)}")

    # Compute from manifest
    manifest_path = cfg["manifest_path"]

    # Batching: token budget + sample cap (both required)
    max_batch_tokens = cfg["max_batch_tokens"]
    batch_size = cfg["batch_size"]
    spatial_factor = cfg.get("spatial_factor", 16)

    plan = plan_clustered_batches(
        manifest_path=manifest_path,
        batch_size=batch_size,
        max_batch_tokens=max_batch_tokens,
        spatial_factor=spatial_factor,
        min_pixels=cfg["filter_min_pixels"],
        max_pixels=cfg["filter_max_pixels"],
        num_clusters=cfg.get("num_clusters", 2000),
        resize_mode=cfg.get("resize_mode", "avg"),
        gpu=cfg.get("gpu_kmeans", True),
        niter=cfg.get("niter", 10),
        multi_image=is_multi_image,
    )

    # Optionally save for reuse
    if plan_path:
        Path(plan_path).parent.mkdir(parents=True, exist_ok=True)
        torch.save(plan, plan_path)
        logger.info(f"Saved batch plan to {plan_path}")

    return plan


def tokenize_loop(
    rank: int,
    world_size: int,
    cfg: Dict[str, Any],
    handler,
) -> Dict[str, Any]:
    """Main per-rank tokenization loop.

    Steps:
        1. Load/compute BatchPlan (from file or from manifest).
        2. ``split_for_workers(world_size)`` → my_batches.
        3. Resume from checkpoint (batch_index, chunk_id, stats).
        4. Setup writer + W&B.
        5. Main loop over my_batches:
           - loader.load_batch(sample_indices) → images [+ texts]
           - Optional augmentation
           - handler.process_batch(...)
           - Periodic checkpoint
        6. Finalize writer, save final checkpoint.
    """
    output_dir = cfg["output_dir"]
    is_multi_image = _resolve_multi_image(cfg)
    split_mode = cfg.get("seqlen_threshold") is not None
    Path(output_dir).mkdir(parents=True, exist_ok=True)

    # Clean up stale .tmp files from killed runs
    if split_mode:
        for subdir in ("stage2", "lct"):
            sub = Path(output_dir) / subdir
            if sub.exists():
                for tmp in sub.glob(f"rank_{rank:04d}_*.tmp"):
                    logger.warning(f"[rank {rank}] Removing stale temp file: {subdir}/{tmp.name}")
                    tmp.unlink()
    for tmp in Path(output_dir).glob(f"rank_{rank:04d}_*.tmp"):
        logger.warning(f"[rank {rank}] Removing stale temp file: {tmp.name}")
        tmp.unlink()

    # ------------------------------------------------------------------
    # 1. Load / compute BatchPlan
    # ------------------------------------------------------------------
    batch_plan = _load_or_compute_batch_plan(cfg, is_multi_image=is_multi_image)

    # ------------------------------------------------------------------
    # 2. Split for workers → my_batches
    # ------------------------------------------------------------------
    worker_splits = batch_plan.split_for_workers(world_size)
    my_batches = worker_splits[rank] if rank < len(worker_splits) else []

    logger.info(
        f"[rank {rank}/{world_size}] Assigned {len(my_batches)} batches "
        f"(total {len(batch_plan.batches)} batches, {batch_plan.total_samples:,} samples)"
    )

    if not my_batches:
        logger.warning(f"[rank {rank}] No batches assigned — exiting early")
        return {"rank": rank, "samples_processed": 0, "tokens_generated": 0}

    # ------------------------------------------------------------------
    # 3. Resume from checkpoint
    # ------------------------------------------------------------------
    resume = cfg.get("resume", False)
    start_batch_index = 0
    start_chunk_id = 0
    start_stage2_chunk_id = 0
    start_lct_chunk_id = 0
    cumulative_stats = WorkerStats()

    if resume:
        ckpt = load_checkpoint(output_dir, rank)
        if ckpt is not None:
            ckpt_ws = ckpt.get("world_size")
            if ckpt_ws is not None and ckpt_ws != world_size:
                logger.warning(
                    f"[rank {rank}] Checkpoint world_size ({ckpt_ws}) != current "
                    f"world_size ({world_size}). Ignoring checkpoint."
                )
                ckpt = None
        # Incompatible resume guard: split_mode vs non-split checkpoint
        if ckpt is not None:
            ckpt_has_split = "stage2_chunk_id" in ckpt
            if split_mode and not ckpt_has_split:
                logger.warning(
                    f"[rank {rank}] Split mode enabled but checkpoint lacks "
                    f"stage2_chunk_id. Ignoring checkpoint."
                )
                ckpt = None
            elif not split_mode and ckpt_has_split:
                logger.warning(
                    f"[rank {rank}] Split mode disabled but checkpoint has "
                    f"stage2_chunk_id. Ignoring checkpoint."
                )
                ckpt = None
        if ckpt is not None:
            start_batch_index = ckpt["batch_index"] + 1
            start_chunk_id = ckpt["chunk_id"] + 1
            if split_mode:
                start_stage2_chunk_id = ckpt["stage2_chunk_id"] + 1
                start_lct_chunk_id = ckpt["lct_chunk_id"] + 1
            prev = ckpt.get("stats", {})
            cumulative_stats.samples_processed = prev.get("samples_processed", 0)
            cumulative_stats.tokens_generated = prev.get("tokens_generated", 0)
            cumulative_stats.image_tokens = prev.get("image_tokens", 0)
            cumulative_stats.text_tokens = prev.get("text_tokens", 0)
            cumulative_stats.errors = prev.get("errors", 0)
            cumulative_stats.samples_skipped = prev.get("samples_skipped", 0)
            cumulative_stats.cuda_oom_errors = prev.get("cuda_oom_errors", 0)
            cumulative_stats.stage2_tokens = prev.get("stage2_tokens", 0)
            cumulative_stats.lct_tokens = prev.get("lct_tokens", 0)
            logger.info(
                f"[rank {rank}] Resumed from batch {start_batch_index}, "
                f"chunk {start_chunk_id}, samples={cumulative_stats.samples_processed}"
            )

    # ------------------------------------------------------------------
    # 4. Create tokenizer on GPU
    # ------------------------------------------------------------------
    from vision_tokenization.vokenizers.emu import create_tokenizer

    mode = cfg["mode"]
    device = f"cuda:{cfg.get('local_rank', 0)}"
    configured_max_images_per_encode = cfg.get("max_images_per_encode")
    effective_max_images_per_encode = (
        configured_max_images_per_encode if is_multi_image else None
    )
    if rank == 0 and not is_multi_image and configured_max_images_per_encode is not None:
        logger.warning(
            "Ignoring tokenizer.max_images_per_encode=%s for single-image dataset; "
            "batch_size and max_batch_tokens control batch memory.",
            configured_max_images_per_encode,
        )
    tokenizer = create_tokenizer(
        mode=mode,
        text_tokenizer_path=cfg["tokenizer_path"],
        device=device,
        min_pixels=cfg["tokenizer_min_pixels"],
        max_pixels=cfg["tokenizer_max_pixels"],
        max_images_per_encode=effective_max_images_per_encode,
        **(cfg.get("tokenizer_kwargs", {})),
    )

    # ------------------------------------------------------------------
    # 5. Setup writer, data loader, augmenter, W&B
    # ------------------------------------------------------------------
    chunk_id = start_chunk_id
    if split_mode:
        handler.setup_writer(
            output_dir, rank, start_stage2_chunk_id, start_lct_chunk_id, tokenizer,
        )
    else:
        handler.setup_writer(output_dir, rank, chunk_id, tokenizer)

    data_loader = create_loader(cfg)
    augmenter = None
    aug_cfg = cfg.get("augmentation")
    if aug_cfg:
        augmenter = ImageAugmenter(**aug_cfg)

    # W&B logger (rank 0 only)
    wandb_logger = None
    wandb_cfg = cfg.get("wandb", {})
    if wandb_cfg.get("enabled", False) and rank == 0:
        wandb_logger = SimpleWandbLogger(
            project=wandb_cfg.get("project", "vision-tokenization"),
            entity=wandb_cfg.get("entity"),
            name=wandb_cfg.get("name"),
            tags=wandb_cfg.get("tags", []),
            config={
                "rank": rank,
                "world_size": world_size,
                "mode": mode,
                **{k: v for k, v in cfg.items() if isinstance(v, (int, float, str, bool))},
            },
            log_interval_seconds=wandb_cfg.get("log_interval_seconds", 10.0),
        )

    # ------------------------------------------------------------------
    # 6. Main loop
    # ------------------------------------------------------------------
    checkpoint_interval = cfg.get("checkpoint_interval_batches", 500)
    timing_enabled = wandb_logger is not None
    stats = cumulative_stats
    batch_count = 0                     # successful batches since resume (for periodic checkpoint cadence)
    last_batch_index = start_batch_index - 1  # last visited batch index (for resume correctness)
    consecutive_errors = 0
    max_consecutive_errors = cfg.get("max_consecutive_errors", 50)
    _loop_error = None

    prefetch_cfg = cfg.get("prefetch", {})
    prefetch_queue_size = prefetch_cfg.get("queue_size", 2)
    prefetch_num_workers = prefetch_cfg.get("num_workers", 1)
    prefetcher = BatchPrefetcher(
        data_loader, augmenter,
        queue_size=prefetch_queue_size,
        num_workers=prefetch_num_workers,
    )

    logger.info(
        f"[rank {rank}] Starting tokenization loop "
        f"(chunk_id={chunk_id}, checkpoint_interval={checkpoint_interval}, "
        f"start_batch={start_batch_index}, "
        f"prefetch_workers={prefetch_num_workers}, prefetch_queue={prefetch_queue_size})"
    )

    try:
        batch_iter = prefetcher.iter_batches(my_batches, start=start_batch_index)
        if rank == 0:
            from tqdm import tqdm
            total_batches = len(my_batches) - start_batch_index
            batch_iter = tqdm(
                batch_iter, total=total_batches, desc="rank 0",
                unit="batch", dynamic_ncols=True,
            )
        for result in batch_iter:
            last_batch_index = result.batch_index

            # Prefetch-stage error — apply same retry logic as GPU errors
            if result.error is not None:
                stats.errors += 1
                consecutive_errors += 1
                logger.warning(
                    f"[rank {rank}] Prefetch error on batch {result.batch_index} "
                    f"({consecutive_errors}/{max_consecutive_errors}): {result.error}"
                )
                if consecutive_errors >= max_consecutive_errors:
                    raise RuntimeError(
                        f"[rank {rank}] {max_consecutive_errors} consecutive batch errors, aborting"
                    ) from result.error
                continue

            try:
                resize_size = (result.assignment.resize_height, result.assignment.resize_width)

                process_timing = handler.process_batch(
                    result.images, resize_size, tokenizer, stats, device,
                    texts=result.texts,
                    group_slices=result.assignment.group_slices,
                    timing_enabled=timing_enabled,
                )
                consecutive_errors = 0

            except Exception as batch_err:
                stats.errors += 1
                consecutive_errors += 1

                if is_cuda_oom(batch_err):
                    torch.cuda.empty_cache()
                    stats.cuda_oom_errors += 1
                    logger.warning(
                        f"[rank {rank}] CUDA OOM on batch {result.batch_index}, freed cache "
                        f"({consecutive_errors}/{max_consecutive_errors})"
                    )
                else:
                    logger.warning(
                        f"[rank {rank}] Batch error ({consecutive_errors}/{max_consecutive_errors}): "
                        f"{batch_err}"
                    )

                if consecutive_errors >= max_consecutive_errors:
                    raise RuntimeError(
                        f"[rank {rank}] {max_consecutive_errors} consecutive batch errors, aborting"
                    ) from batch_err
                continue

            batch_count += 1

            # W&B log (rate-limited) — merge prefetch + handler timing
            if wandb_logger is not None:
                timing = {
                    "load_ms": result.timing["load_s"] * 1000,
                    "augment_ms": result.timing["augment_s"] * 1000,
                }
                if process_timing:
                    timing["tokenize_gpu_ms"] = process_timing["tokenize_gpu_s"] * 1000
                    timing["tokenize_wall_ms"] = process_timing["tokenize_wall_s"] * 1000
                    timing["write_ms"] = process_timing["write_s"] * 1000
                wandb_logger.log(
                    samples=stats.samples_processed,
                    tokens=stats.tokens_generated,
                    image_tokens=stats.image_tokens,
                    text_tokens=stats.text_tokens,
                    errors=stats.errors,
                    skipped=stats.samples_skipped,
                    timing=timing,
                )

            # Periodic checkpoint
            if batch_count % checkpoint_interval == 0 and handler.chunk_samples > 0:
                done_chunk = handler.checkpoint_writer()
                if split_mode:
                    s2_done, lct_done = done_chunk
                    logger.info(
                        f"[rank {rank}] Finalized split chunks "
                        f"(stage2={s2_done}, lct={lct_done}, "
                        f"{stats.tokens_generated:,} total tokens)"
                    )
                    save_checkpoint(
                        output_dir, rank,
                        batch_index=result.batch_index,
                        chunk_id=result.batch_index,  # not meaningful in split mode
                        stats=stats.to_dict(),
                        world_size=world_size,
                        extra={
                            "stage2_chunk_id": s2_done,
                            "lct_chunk_id": lct_done,
                        },
                    )
                else:
                    logger.info(
                        f"[rank {rank}] Finalized chunk {done_chunk} "
                        f"({stats.tokens_generated:,} total tokens)"
                    )
                    save_checkpoint(
                        output_dir, rank,
                        batch_index=result.batch_index,
                        chunk_id=done_chunk,
                        stats=stats.to_dict(),
                        world_size=world_size,
                    )
                    chunk_id = done_chunk + 1

    except Exception as e:
        logger.error(f"[rank {rank}] Fatal error in tokenization loop: {e}", exc_info=True)
        stats.errors += 1
        _loop_error = e
    finally:
        prefetcher.shutdown()

    # ------------------------------------------------------------------
    # 7. Finalize (always save progress, even on failure)
    # ------------------------------------------------------------------
    handler.finalize_writer()

    if split_mode:
        save_checkpoint(
            output_dir, rank,
            batch_index=last_batch_index,
            chunk_id=last_batch_index,
            stats=stats.to_dict(),
            world_size=world_size,
            extra={
                "stage2_chunk_id": handler.writer.stage2_chunk_id,
                "lct_chunk_id": handler.writer.lct_chunk_id,
            },
        )
    else:
        save_checkpoint(
            output_dir, rank,
            batch_index=last_batch_index,
            chunk_id=chunk_id,
            stats=stats.to_dict(),
            world_size=world_size,
        )

    result = stats.finalize()
    result["rank"] = rank
    if split_mode:
        result["stage2_chunks_written"] = (
            handler.writer.stage2_chunk_id - start_stage2_chunk_id
            + (1 if handler.writer.stage2_chunk_samples > 0 else 0)
        )
        result["lct_chunks_written"] = (
            handler.writer.lct_chunk_id - start_lct_chunk_id
            + (1 if handler.writer.lct_chunk_samples > 0 else 0)
        )
    else:
        result["chunks_written"] = chunk_id - start_chunk_id + (1 if handler.chunk_samples > 0 else 0)

    if wandb_logger is not None:
        wandb_logger.finish()

    data_loader.close()

    text_tok_msg = ""
    if result.get("text_tokens", 0) > 0:
        text_tok_msg = f", {result['text_tokens']:,} text tokens"
    logger.info(
        f"[rank {rank}] Done: {result['samples_processed']:,} samples, "
        f"{result['image_tokens']:,} image tokens{text_tok_msg}, "
        f"{result['errors']} errors, {result['elapsed_time']:.1f}s"
    )
    result["output_dir"] = output_dir

    if _loop_error is not None:
        raise RuntimeError(
            f"[rank {rank}] Tokenization loop failed after processing "
            f"{result['samples_processed']} samples"
        ) from _loop_error

    return result
